import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const repoRoot = resolve(dirname(scriptPath), '..');

const markdown = await readFile(resolve(repoRoot, 'dor/skill.md'), 'utf8');
const outPath = resolve(repoRoot, 'dor/src/generated-skill.ts');

await mkdir(dirname(outPath), { recursive: true });
await writeFile(
  outPath,
  `// Generated by scripts/generate-dor-skill.mjs. Do not edit.\n` +
    `export const DOR_SKILL_MARKDOWN = ${JSON.stringify(markdown)};\n`,
);
