import { execFileSync } from 'node:child_process';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const repoRoot = resolve(dirname(scriptPath), '..');

const changelog = await readFile(resolve(repoRoot, 'CHANGELOG.md'), 'utf8');
const version = latestChangelogVersion(changelog);
const commit = git(['rev-parse', '--short=8', 'HEAD']) || 'unknown';
const commitsSinceVersion = Number(git(['rev-list', '--count', `v${version}..HEAD`]) || '0');
const outPath = resolve(repoRoot, 'dor/src/generated-version.ts');

await mkdir(dirname(outPath), { recursive: true });
await writeFile(
  outPath,
  `// Generated by scripts/generate-dor-version.mjs. Do not edit.\n` +
    `export const DOR_VERSION_METADATA = ${JSON.stringify({
      version,
      commit,
      commitsSinceVersion: Number.isFinite(commitsSinceVersion) ? commitsSinceVersion : 0,
    }, null, 2)} as const;\n`,
);

function latestChangelogVersion(input) {
  for (const line of input.split('\n')) {
    const match = /^## \[([0-9]+\.[0-9]+\.[0-9]+)\]/.exec(line);
    if (match) return match[1];
  }
  return '0.0.0';
}

function git(args) {
  try {
    return execFileSync('git', args, { cwd: repoRoot, encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
}
